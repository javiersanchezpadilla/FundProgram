""" Ejemplo del uso de ciclos con valores iniciales, finales y pasos personalizados.
    Se muestran ejemplos de uso de range con diferentes parámetros de inicio, fin y paso."""


print("Iterando sobre un rango de números del 0 al 10 con paso 2:")
for valor in range(0, 11, 2):
    print(valor)    


print("\nIterando sobre un rango de números del 5 al 25 con paso 5:")
for valor in range(5, 26, 5):
    print(valor)    

print("\nIterando sobre un rango de números del 10 al 50 con paso 10:")
for valor in range(10, 51, 10):
    print(valor)

print("\nIterando sobre un rango de números del -40 al 100 con paso 20:")
for valor in range(-40, 101, 20):
    print(valor)    

