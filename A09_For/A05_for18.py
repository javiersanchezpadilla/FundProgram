""" Uso de range con inicio y fin distintos a cero"""

print("Iterando sobre un rango de números del 2 al 7:")
for valor in range(2, 8):
    print(valor)

print("\nIterando sobre un rango de números del 5 al 15:")
for valor in range(5, 16):
    print(valor)

print("\nIterando sobre un rango de números del 10 al 20:")
for valor in range(10, 21):
    print(valor)
