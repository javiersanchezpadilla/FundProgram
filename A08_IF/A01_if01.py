""" Uso de los operadores relacionales.
    En este ejercicio se realizaran operaciones mediante
    el uso de los operadores relacionales."""

a = 4
b = 2
res = a == b
print(f'{a} es == {b} ? {res}')     # False

res = a != b
print(f'{a} es != {b} ? {res}')     # True

res = a < b
print(f'{a} es < {b} ? {res}')      # False

res = a > b
print(f'{a} es > {b} ? {res}')      # True

res = a >= b
print(f'{a} es >= {b} ? {res}')     # True

res = a <= b
print(f'{a} es <= {b} ? {res}')     # False

