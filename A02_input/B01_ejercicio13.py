""" Conversión Hexadecimal: Pide un número decimal entero. 
    Imprime su valor en hexadecimal usando la función hex()."""

numero_decimal = input("Escribe un número decimal entero: ")
numero_hexadecimal = hex(int(numero_decimal))
print(f"El número {numero_decimal} en hexadecimal es {numero_hexadecimal}.")

